package com.spring.movietime.vo;

public class MovieTimeVo {

	private String time_id;
	private String movie_id;
	private String sc_id;
	private int    sc_num;
	private String m_s_time;
	private String m_f_time;
	private String sc_name;
	
	
	
	
	
	public String getTime_id() {
		return time_id;
	}
	public void setTime_id(String time_id) {
		this.time_id = time_id;
	}
	public String getMovie_id() {
		return movie_id;
	}
	public void setMovie_id(String movie_id) {
		this.movie_id = movie_id;
	}
	public String getSc_id() {
		return sc_id;
	}
	public void setSc_id(String sc_id) {
		this.sc_id = sc_id;
	}
	public int getSc_num() {
		return sc_num;
	}
	public void setSc_num(int sc_num) {
		this.sc_num = sc_num;
	}
	public String getM_s_time() {
		return m_s_time;
	}
	public void setM_s_time(String m_s_time) {
		this.m_s_time = m_s_time;
	}
	public String getM_f_time() {
		return m_f_time;
	}
	public void setM_f_time(String m_f_time) {
		this.m_f_time = m_f_time;
	}
	public String getSc_name() {
		return sc_name;
	}
	public void setSc_name(String sc_name) {
		this.sc_name = sc_name;
	}
	
	
	
}
